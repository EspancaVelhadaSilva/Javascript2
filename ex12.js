//Criar um programa com uma variável e mostrar se o número é maior ou menor que 10.
let num1 = 9
if (num1 > 10){
    console.log (`O número ${num1} é maior que 10`)
}
else if (num1 < 10){
    console.log (`O número ${num1} é menor que 10`)
}
else if (num1 = 10){
    console.log(`O número ${num1} é igual que 10`)
}